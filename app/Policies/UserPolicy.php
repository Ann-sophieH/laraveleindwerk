<?php

namespace App\Policies;

use App\Models\User;
use Illuminate\Auth\Access\HandlesAuthorization;
use Illuminate\Auth\Access\Response;

class UserPolicy
{
    use HandlesAuthorization;

    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function viewAny(User $user)
    {
        //
       // return $model->user->is($user);
        foreach($user->roles as $role){
            return $role->name === 'author'
                ? Response::allow()
                : Response::deny('You do not have this permission, you cannot see this info.');
        }

    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(User $user, User $model)
    {
        //
        return $model->id === $user->id
            ? Response::allow()
            : Response::deny('You do not own this account, you cannot view this info.');
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function create(User $user)
    {
        //
        foreach($user->roles as $role){
            return $role->name === 'author'
                ? Response::allow()
                : Response::deny('You do not have this permission, you cannot make this info.');
        }

    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function update(User $user, User $model)
    {
        //model == user update thats tryna be accessed
        return $model->id === $user->id
            ? Response::allow()
            : Response::deny('You do not own this account, you cannot edit this info.');

    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function delete(User $user, User $model)
    {
        //
        return $model->id === $user->id // model is the account to be deleted / $user is the Auth usr
            ? Response::allow()
            : Response::deny('You do not own this account, you cannot delete this info.');

    }

